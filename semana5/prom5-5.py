#Un maestro desea saber que porcentaje de hombres y que porcentaje de mujeres hay en un grupo de estudiantes.
a=float(input("ingrese el numero de hombres"))
b=float(input("ingrese el numero de mujeres"))
ta=a+b
ph=a*100/ta
pm=b*100/ta

print("el porcentaje de hombres es :" , ph)
print("el porcentaje de mujeres es :" , pm)